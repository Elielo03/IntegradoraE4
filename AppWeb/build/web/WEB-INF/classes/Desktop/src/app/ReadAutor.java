    private void actualizarTabla() {
        daoAutor = new DaoAutor(con);

       

        List<BeanAutor> lista = daoAutor.getAll();

        for (BeanAutor bean : lista) {

            Object[] arreglo
                    = {bean.getId_autor(), bean.getNombre(), bean.getPrimer_apellido(), bean.getSegundo_apellido(),
                        bean.getPais()};
            modelo.addRow(arreglo);

        }
        tblAutor.setModel(modelo);
    }
    public void busqueda(String bus){
        
        if(bus.length()<0){
            actualizarTabla();
        }else{
        modelo= new DefaultTableModel(new String[]{"Autor_id","Nombre","Primer Apellido"},0);
        List<BeanAutor> lista= daoAutor.getAll();
        for (BeanAutor bean : lista) {
            if(bean.getNombre().toLowerCase().contains(bus.toLowerCase())){
                Object [] arreglo= {bean.getId_autor(),bean.getNombre(),bean.getPrimer_apellido()};
                modelo.addRow(arreglo);
            }
            
        }
        }
        
        tblAutor.setModel(modelo);
        
    }

