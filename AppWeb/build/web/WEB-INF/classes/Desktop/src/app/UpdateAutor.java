    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
       DaoAutor daoAutor= new DaoAutor(conexion);
        
        String nombre =txtNombre.getText();
        consultaAutor.setNombre(nombre);
       
        String apellido1= txtApellido1.getText();
       consultaAutor.setPrimer_apellido(apellido1);
        String apellido2 = txtApellido2.getText();
        consultaAutor.setSegundo_apellido(apellido2);
        
        String pais= txtPais.getText();
        consultaAutor.setPais(pais);
        
        boolean confirm= daoAutor.update(consultaAutor);
        if(confirm){
            JOptionPane.showMessageDialog(rootPane, "Autor modificado con Ã©xito");
            new ReadAutor().setVisible(true);
            this.dispose();
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Error al modificar el autor");
        }
        
        
    }//GEN-LAST:event_btnAceptarActionPerformed

