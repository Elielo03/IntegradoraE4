    private void actualizarTabla() {
        daoArea = new DaoArea(con);

      

        List<BeanArea> lista = daoArea.getActive();

        for (BeanArea bean : lista) {

            Object[] arreglo
                    = {bean.getId_area(), bean.getNombre(), bean.getAlta()};
                modelo.addRow(arreglo);
            
        }
        tblAreas.setModel(modelo);
    }
    
    public void busqueda(String bus){
        if(bus.length()<0){
            actualizarTabla();
        }else{
        
        modelo= new DefaultTableModel (new String[]{"Autor_id","Nombre","Alta"},0);
        List<BeanArea> lista= daoArea.getActive();
        for (BeanArea bean : lista) {
            if(bean.getNombre().toLowerCase().contains(bus.toLowerCase())){
                Object[] arreglo= {bean.getId_area(), bean.getNombre(), bean.getAlta()};
                modelo.addRow(arreglo);
            }
            
        }
        }
        tblAreas.setModel(modelo);
    }

