    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Comprobacion com = new Comprobacion();
        dao=new DaoEstante(con);
        String ubicacion= txtUbicacion.getText();
        if(ubicacion.length()==0){
           String mensaje="Error de campos obligatorios vacios:\n";
           if(com.validarCampoTxt(ubicacion)){
               mensaje+="*Ubicacion\n";
           }
           JOptionPane.showMessageDialog(rootPane, mensaje);
        }else{
        
        bean=new BeanEstante(0,ubicacion);
       if(dao.add(bean)){
           JOptionPane.showMessageDialog(rootPane, "Ubicacion guardada con exito");
           this.dispose();
       }else{
           JOptionPane.showMessageDialog(rootPane, "Error añ guardar la ubicacion");
       }
        }
    }//GEN-LAST:event_btnGuardarActionPerformed

