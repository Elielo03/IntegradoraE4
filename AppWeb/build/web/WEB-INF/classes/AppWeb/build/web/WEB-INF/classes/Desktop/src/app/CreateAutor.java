    private void btnAceptarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAceptarActionPerformed
        // TODO add your handling code here:
        Comprobacion com = new Comprobacion();
       DaoAutor daoAutor= new DaoAutor(conexion);
        
        String nombre =txtNombre.getText();
       
        String apellido1= txtApellido1.getText();
       
        String apellido2 = txtApellido2.getText();
        
        String pais= txtPais.getText();
        
        if(nombre.length()==0 || apellido1.length()==0 || pais.length()==0){
           String mensaje="Error de campos obligatorios vacios:\n";
           if(com.validarCampoTxt(nombre)){
               mensaje+="*Nombre\n";
           }
           if(com.validarCampoTxt(apellido1)){
               mensaje+="*Apellido1\n";
           }
           if(com.validarCampoTxt(pais)){
               mensaje+="*Pais\n";
           }
           
           JOptionPane.showMessageDialog(rootPane, mensaje);
        }else{
        beanAutor= new BeanAutor(0,nombre,apellido1,apellido2,pais);
        boolean confirm= daoAutor.add(beanAutor);
        if(confirm){
            JOptionPane.showMessageDialog(rootPane, "Autor agregado con exito :)");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Error al agregar el autor");
        }
        this.dispose();
        }
    }//GEN-LAST:event_btnAceptarActionPerformed

