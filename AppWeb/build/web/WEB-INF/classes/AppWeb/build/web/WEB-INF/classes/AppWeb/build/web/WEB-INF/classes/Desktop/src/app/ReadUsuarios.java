    private void actualizarTabla() {
        daoUsuario = new DaoUsuario(con);

      

        List<BeanUsuario> lista = daoUsuario.getActive();

        for (BeanUsuario bean : lista) {

            Object[] arreglo
                    = {bean.getId_usuario(), bean.getNombre(), bean.getPrimer_apellido(), bean.getSegundo_apellido(),
                        bean.getCorreo(), bean.isEs_admi(), bean.getPass(), bean.getDireccion(), bean.getTelefono(),
                        bean.isAlta()};
                modelo.addRow(arreglo);
            
        }
        tblUsuarios.setModel(modelo);
    }
    
    public void busqueda(String bus){
        if(bus.length()<0){
            actualizarTabla();
        }else{
        
        modelo= new DefaultTableModel (new String[]{"Usuario_id","Nombre","Primer Apellido", "Segundo Apellido", "Correo", 
        "Es admin", "Contraseña", "Direccion", "Telefono", "Estado"},0);
        List<BeanUsuario> lista= daoUsuario.getActive();
        for (BeanUsuario bean : lista) {
            if(bean.getNombre().toLowerCase().contains(bus.toLowerCase()) || bean.getPrimer_apellido().toLowerCase().contains(bus.toLowerCase())
                    || bean.getSegundo_apellido().toLowerCase().contains(bus.toLowerCase())){
                Object[] arreglo= {bean.getId_usuario(), bean.getNombre(), bean.getPrimer_apellido(),bean.getSegundo_apellido(),
                        bean.getCorreo(), bean.isEs_admi(), bean.getPass(), bean.getDireccion(), bean.getTelefono(),
                        bean.isAlta()};
                modelo.addRow(arreglo);
            }
            
        }
        }
        tblUsuarios.setModel(modelo);
    }
   

