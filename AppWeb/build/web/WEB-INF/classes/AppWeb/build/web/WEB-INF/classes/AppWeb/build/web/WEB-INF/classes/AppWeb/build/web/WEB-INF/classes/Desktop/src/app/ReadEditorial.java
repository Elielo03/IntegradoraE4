    private void actualizarTabla() {
        daoEditorial = new DaoEditorial(con);

      

        List<BeanEditorial> lista = daoEditorial.getActive();

        for (BeanEditorial bean : lista) {

            Object[] arreglo
                    = {bean.getId_editorial(), bean.getNombre(), bean.getDireccion()};
                modelo.addRow(arreglo);
            
        }
        tblEditorial.setModel(modelo);
    }
    
    public void busqueda(String bus){
        if(bus.length()<0){
            actualizarTabla();
        }else{
        
        modelo= new DefaultTableModel (new String[]{"Editorial_id","Nombre","Direccion"},0);
        List<BeanEditorial> lista= daoEditorial.getActive();
        for (BeanEditorial bean : lista) {
            if(bean.getNombre().toLowerCase().contains(bus.toLowerCase())){
                Object[] arreglo= {bean.getId_editorial(),bean.getNombre(), bean.getDireccion()};
                modelo.addRow(arreglo);
            }
            
        }
        }
        tblEditorial.setModel(modelo);
    }

