    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        Comprobacion com = new Comprobacion();
        DaoArea daoArea = new DaoArea(conexion);
        String nombre= txtNombre.getText();
        consultaArea.setNombre(nombre);
        
        
        if(nombre.length()==0){
           String mensaje="Error de campos obligatorios vacios:\n";
           if(com.validarCampoTxt(nombre)){
               mensaje+="*Nombre\n";
           }
           JOptionPane.showMessageDialog(rootPane, mensaje);
        }else{
        boolean confirm= daoArea.updatear(consultaArea);
        if(confirm){
            JOptionPane.showMessageDialog(rootPane, "Area modificada con éxito");
            new ReadArea().setVisible(true);
            this.dispose();
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Error al modificar el Area");
        }
        
        }
        
    }//GEN-LAST:event_btnGuardarActionPerformed

    private void btnborrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnborrarActionPerformed
        DaoArea daoArea= new DaoArea(conexion);       
        
        boolean confirm = daoArea.baja(consultaArea.getId_area());
        if(confirm){
            JOptionPane.showMessageDialog(rootPane, "Area Eliminada con éxito");
            new ReadArea().setVisible(true);
            this.dispose();
            
        }else{
            JOptionPane.showMessageDialog(rootPane, "Error al modificar el Area");
        }
    }//GEN-LAST:event_btnborrarActionPerformed

