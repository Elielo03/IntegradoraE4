    private void btnAgregarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarActionPerformed
        // TODO add your handling code here:         
         BeanLibro libro= new BeanLibro();
         DaoLibro daoLibro = new DaoLibro(conexion);       
         
         
         BeanEjemplar ejem = new BeanEjemplar();
        DaoEjemplar Daoejem = new DaoEjemplar(conexion);
        
         BeanEstante estan = new BeanEstante();
         DaoEstante daoEst = new DaoEstante(conexion);
        
         estan= daoEst.findByNombre((String) cmbEstante.getSelectedItem());
         
        libro=daoLibro.findByNombre((String) cmbLibro.getSelectedItem());
        
         
        ejem.setEstante(estan);
        ejem.setLibro(libro);

       int num=Integer.parseInt(txtNum.getText());
       if(txtNum.getText().length()==0){
           JOptionPane.showMessageDialog(rootPane, "Ingresa una catidad de emeplares");
       }else{
            for (int i = 0; i < num; i++) {
           if(Daoejem.add(ejem)){
               new InicioAdmin().setVisible(true);
               this.dispose();
           }else{
               JOptionPane.showMessageDialog(rootPane, "Error");
           }
            
        }
       }
       
       
        
      
        
    }//GEN-LAST:event_btnAgregarActionPerformed

