    private void actualizarTabla() {
        daoLibro = new DaoLibro(con);
        List<BeanLibro> lista = daoLibro.maxEjemplar();
        
        for (BeanLibro bean : lista) {

            Object[] arreglo
                    = {bean.getTitulo()};
                modelo.addRow(arreglo);
            
        }
        tblmaxejem.setModel(modelo);
    }
    
    private void actualizarTabla2() {
        
        daoEditorial = new DaoEditorial(con);
        List<BeanEditorial> lista = daoEditorial.minEditorial();
        
        for (BeanEditorial bean : lista) {

            Object[] arreglo
                    = {bean.getNombre()};
                mod.addRow(arreglo);
            
        }
        tblminedit.setModel(mod);
    }

